//This code creates & initializes all primitive types. Also, it may print out a value of a variable, if you want.
public class Task_2 {
    public static void main(String[] args) {
        int a = 317;
        byte be = 101;
        short s = 4001;
        float f = 5.231900f;
        double d = 2.77334457902782;
        long l = -12342345000L;
        boolean value = false;
        char b = 'y';
        System.out.println("int value is: " + a);
        System.out.println("byte value is: " + be);
        System.out.println("short value is: " + s);
        System.out.println("float value is: " + f);
        System.out.println("double value is: " + d);
        System.out.println("long value is: " + l);
        System.out.println("boolean value is: " + value);
        System.out.println("char value is: " + b);
    }
}
